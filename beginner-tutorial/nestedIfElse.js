var weekno = 10;
if (weekno == 1) {
    console.log("Sunday");
}
else if (weekno == 2) {
    console.log("Monday");
}
else if (weekno == 3) {
    console.log("Tue");
}
else if (weekno == 4) {
    console.log("Wed");
}
else if (weekno == 5) {
    console.log("Thr");
}
else if (weekno == 6) {
    console.log("Fri");
}
else if (weekno == 7) {
    console.log("Sat");
}
else {
    console.log("invalid week number");
}
