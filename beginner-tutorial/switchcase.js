var weekno = 10;
switch (weekno) {
    case 1:
        console.log("Sunday");
        break;
    case 2:
        console.log("Monday");
        break;
    case 3:
        console.log("Tuesday");
        break;
    case 4:
        console.log("Web");
        break;
    case 5:
        console.log("Thr");
        break;
    case 6:
        console.log("Fri");
        break;
    case 6:
        console.log("Sat");
        break;
    default: console.log("Invalid week number");
}
